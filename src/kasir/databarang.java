    private void tampilTabel(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama barang");
        model.addColumn("Harga barang");
        model.addColumn("Stok");
        
        try {
            String sql = "select * from barang";
            Connection conn = (Connection)koneksi.configDB();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()){
                model.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                });
            }
            tabelinput.setModel(model);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error",0);
        }
    }
    
    private void pilihData(){
        int baris = tabelinput.getSelectedRow();
        idText.setText(tabelinput.getValueAt(baris, 0).toString());
        namaText.setText(tabelinput.getValueAt(baris, 1).toString());
        hargaText.setText(tabelinput.getValueAt(baris, 2).toString());
        stokText.setText(tabelinput.getValueAt(baris, 3).toString());
    }
    
    private void kosongkanForm(){
        idText.setText(null);
        namaText.setText(null);
        hargaText.setText(null);
        stokText.setText(null);
    }
    
