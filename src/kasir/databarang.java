    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        try {
            String sql = "delete from barang where id_barang = "+idText.getText();
            Connection conn = (Connection)koneksi.configDB();
            PreparedStatement pst=conn.prepareStatement(sql);
            Object[] tombol = {"Ya","Tidak"};
            int yatidak = JOptionPane.showOptionDialog(null, "Anda yakin ingin menghapus data ini?", "Konfirmasi hapus",
                    0, 2, null, tombol, tombol[1]);
            if(yatidak == JOptionPane.NO_OPTION){
                this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
            } else if(yatidak == JOptionPane.YES_OPTION){
                pst.execute();
                conn.prepareStatement("ALTER TABLE barang DROP id_barang").execute();
                conn.prepareStatement("ALTER TABLE barang ADD id_barang INT NOT NULL AUTO_INCREMENT PRIMARY KEY FIRST").execute();
                tampilTabel();
                kosongkanForm();
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data:\n"+e.getMessage(), "Error", 0);
        }
    }//GEN-LAST:event_bhapusActionPerformed
    
